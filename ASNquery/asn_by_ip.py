def download_photo_base64(url):
    '''
    :param url:
    :return: base64 - bytes of image
    '''
    try:
        resource = requests.get(url, stream=True)
    except:
        print('exception in get data...')
    else:
        if resource.status_code == 200:
            resource.raw.decode_content = True
            image_bytes = resource.raw.read()
            encoded_image = base64.b64encode(image_bytes)
            return codecs.decode(encoded_image)


def grouper(count, iterable, fillvalue=None):
    """
    :param count: length of subblock
    :param iterable: array of data
    :param fillvalue: is fill value in last chain
    :return:
    grouper(3, 'ABCDEFG', 'x') --> ABC DEF Gxx"
    """
    args = [iter(iterable)] * count
    result = []
    for element in itertools.zip_longest(fillvalue=fillvalue, *args):
        tmp = filter(lambda y: y is not None, element)
        result.append(list(tmp))
    return result


def return_asn_for_block_ips(ips, log):
    cache_images = {}
    headers = {'user-agent': choice(get_user_agents()),
               "Accept": "text/html,application/xhtml+xml,application/xml;q=0.9,*/*;q=0.8",
               "Accept-Language": """en - US;q = 0.5, en;q = 0.3"""}
    session_request = requests.Session()
    result_rows = []
    for ip in ips:
        check = False
        count_repeat = 4
        sleep_sec = 1
        check_i = 0
        text = None
        code = None
        url = template_ip.format(ip)
        while not check and check_i < count_repeat:
            try:
                req = session_request.get(url, headers=headers, timeout=5)
                status_code = req.status_code
                check = True
            except Exception as ex:
                log.info(f"try {check_i}, error:'{str(ex)}' connecting - error, sleep - 1 sec.")
                time.sleep(sleep_sec)
                check_i += 1

        if check and status_code == 200:
            text = req.text
        if text:
            try:
                soup = BeautifulSoup(text, 'html.parser')
                second_record_ip = soup.find("h4").text
                # 2
                _header_html = soup.find("div", {"class": "col-sm-12 box",
                                                "id": "content-info"})
                table_block = _header_html.find("table", {"class":"table table-hover"})
                _need_rows = table_block.find("tbody")
                need_rows = _need_rows.find_all("tr")
                keys = ["Country",
                        "Country_flag",
                        "Announced_Prefix",
                        "Prefix_Name",
                        "Prefix_Description",
                        "ASN",
                        "ASN_Description",
                        "ASN_Name",
                        "Country_ISO",
                        "rDNS",
                        "ip"]

                for row in need_rows:
                    cols = row.find_all("td")
                    if len(cols) == 7:
                        row_dict = {}
                        c = cols[0].find("img")
                        row_dict[keys[0]] = c.get('title')
                        try:
                            code = c.get('src').split('/')[-1].split('.')[0]
                            flag_url = c.get('src')
                            if code not in cache_images:
                                row_dict[keys[1]] = download_photo_base64(flag_url)
                                cache_images[code] = row_dict[keys[1]]
                            else:
                                row_dict[keys[1]] = cache_images[code]
                        except:
                            pass
                        row_dict[keys[2]] = cols[1].text
                        row_dict[keys[3]] = cols[2].text
                        row_dict[keys[4]] = cols[3].text
                        row_dict[keys[5]] = cols[4].text
                        row_dict[keys[6]] = cols[5].text
                        row_dict[keys[7]] = cols[6].text
                        row_dict[keys[8]] = code
                        row_dict[keys[9]] = second_record_ip
                        row_dict[keys[10]] = ip
                        result_rows.append(row_dict)

            except:
                log.info("errors: {}".format(ip))
                result_rows.append({"ip":ip})
        time.sleep(0.3)
    return result_rows


def return_mapping_ip_to_asn(blocks_ip, log, threads=8):
    for_save_to_table = []
    with concurrent.futures.ThreadPoolExecutor(max_workers=threads) as executor:
        future_rows = {executor.submit(return_asn_for_block_ips, block, log):
                           i for i, block in enumerate(blocks_ip)}
        for future in concurrent.futures.as_completed(future_rows):
            n_block = future_rows[future]
            result = future.result()
            if result:
                log.info("ready N: {} from: {}".format(n_block, len(blocks_ip)))
                for_save_to_table.extend(result)
            else:
                log.error("errors:{}".format(n_block))
    return for_save_to_table


